import Foundation
enum DateFormats {
    /// Example: "Mon", "Tue"
    static let dayOfWeekShort = "E"
    
    /// Example: "Monday", "Tuesday"
    static let dayOfWeekFull = "EEEE"
    
    /// Example: "27 Aug 2025"
    static let dayMonthYear = "dd MMM yyyy"
}
